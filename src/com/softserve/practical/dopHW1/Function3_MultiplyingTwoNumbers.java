package com.softserve.practical.dopHW1;

/**
 * Implement a function called multiply, which takes two numbers and returns their product:
 */
public class Function3_MultiplyingTwoNumbers {
    public static void main(String[] args) {
        System.out.println(multiply(2, 2));
    }

    public static int multiply(int numberOne, int numberTwo) {
        return numberOne * numberTwo;
    }

}
