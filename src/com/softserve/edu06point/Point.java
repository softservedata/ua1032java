package com.softserve.edu06point;

public class Point {
    private int x;          // coodinate x;
    private int y;          // coodinate y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
